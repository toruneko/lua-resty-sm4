local function P(a)
    return bxor(lshift(Sbox[rshift(a, 24)], 24),
        lshift(Sbox[band(rshift(a, 16), 0xFF)], 16),
        lshift(Sbox[band(rshift(a, 8), 0xFF)], 8),
        Sbox[band(a, 0xFF)])
end

